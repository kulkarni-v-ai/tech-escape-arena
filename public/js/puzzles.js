function getPuzzlesForTeam(teamId) {
  // Simple deterministic hash
  let hash = 0;
  for (let i = 0; i < teamId.length; i++) {
    hash = ((hash << 5) - hash) + teamId.charCodeAt(i);
    hash |= 0; 
  }
  hash = Math.abs(hash);
  
  const idx = hash % 40;
  
  // --- Puzzle 1: Cipher ---
  const shift = (hash % 5) + 2; // Shift between 2 and 6
  const word1 = TECH_WORDS[idx];
  
  // Apply shift backwards, then reverse
  // Challenge is what the user SEES. User must un-reverse and un-shift.
  // Actually, let's just make the challenge string the encrypted one.
  // Encrypted = word1 shifted backwards, then reversed.
  // So to solve, user shifts forwards, then reverses? 
  // Let's standardise: User must Shift -shift, then reverse.
  // So challenge is generated by: Reverse word1, then Shift +shift.
  let p1Challenge = word1.split('').reverse().map(c => {
    let code = c.charCodeAt(0) + shift;
    if (code > 90) code -= 26;
    return String.fromCharCode(code);
  }).join('');
  const p1Answer = word1;

  // --- Puzzle 2: Logic Grid ---
  // Simple multiplier logic.
  const a = (hash % 6) + 3;
  const b = (hash % 5) + 4;
  const a2 = a + 1; const b2 = b + 1;
  const a3 = a + 2; const b3 = b + 2;
  const p2Answer = String(a3 * b3);

  // --- Puzzle 3: QR Code ---
  const qrWord = TECH_WORDS[(idx + 15) % 40]; // Offset to ensure different word

  // --- Puzzle 4: Riddle ---
  const riddle = RIDDLES[idx];

  const puzzles = [
    {
      id: 1,
      title: 'Cipher Protocol Alpha',
      difficulty: 'MEDIUM',
      description: `An intercepted transmission uses a multi-layered cipher. Shift backwards by ${shift}, then reverse the string.`,
      challenge: p1Challenge,
      customHtml: '',
      hintText: `Apply a Caesar shift of -${shift} to each letter, then reverse the resulting string.`,
      answer: p1Answer,
      hint: `Shift down the alphabet by ${shift}, then reverse. The word is ${p1Answer.substring(0,2)}...`,
      validate(input) { return input.trim().toUpperCase() === this.answer; }
    },
    {
      id: 2,
      title: 'Logic Core',
      difficulty: 'HARD',
      description: 'The core processor requires a missing frequency. Identify the pattern in the grid.',
      challenge: 'Find the missing value (?)',
      customHtml: `
        <div class="logic-grid-wrapper" style="display:flex; flex-direction:column; gap:8px; margin-bottom:15px; font-family:var(--font-mono); font-size:1.4rem; color:var(--neon); font-weight:bold; text-align:center;">
          <div style="display:flex; justify-content:space-around; background:rgba(255,26,26,0.05); padding:12px; border:1px solid rgba(255,26,26,0.2); border-radius:6px;"><span>${a}</span><span style="color:#666;">|</span><span>${b}</span><span style="color:#666;">|</span><span>${a * b}</span></div>
          <div style="display:flex; justify-content:space-around; background:rgba(255,26,26,0.05); padding:12px; border:1px solid rgba(255,26,26,0.2); border-radius:6px;"><span>${a2}</span><span style="color:#666;">|</span><span>${b2}</span><span style="color:#666;">|</span><span>${a2 * b2}</span></div>
          <div style="display:flex; justify-content:space-around; background:rgba(255,26,26,0.05); padding:12px; border:1px solid rgba(255,26,26,0.2); border-radius:6px;"><span>${a3}</span><span style="color:#666;">|</span><span>${b3}</span><span style="color:#666;">|</span><span>?</span></div>
        </div>
      `,
      hintText: 'Look at the relationship between the first two numbers in each row and the third.',
      answer: p2Answer,
      hint: 'Multiply the first number by the second number.',
      validate(input) { return input.trim() === this.answer; }
    },
    {
      id: 3,
      title: 'Visual Data Link',
      difficulty: 'HARDER',
      description: 'Scan the visual data link. Ensure your scanner translates it carefully.',
      challenge: 'Decode the image.',
      customHtml: `
        <div style="text-align:center; margin-bottom:15px; display:flex; justify-content:center;">
          <div id="qr-code-payload" data-qr="${qrWord}" style="padding:10px; background:#fff; border-radius:4px; box-shadow:0 0 15px rgba(255,255,255,0.2);"></div>
        </div>
      `,
      hintText: 'A standard QR Code reader app or your phone camera can scan this.',
      answer: qrWord,
      hint: `The QR code contains the word "${qrWord}".`,
      validate(input) { return input.trim().toUpperCase() === this.answer; }
    },
    {
      id: 4,
      title: 'Final Enigma',
      difficulty: 'FINAL',
      description: 'Solve the riddle to obtain the final access code.',
      challenge: `"${riddle.q}"`,
      customHtml: '',
      hintText: 'Read carefully, it is a classic riddle.',
      answer: riddle.a,
      hint: `The answer is ${riddle.a}.`,
      validate(input) { return input.trim().toUpperCase() === this.answer || input.trim().toUpperCase() === ("A " + this.answer); }
    }
  ];

  const finalCode = `${p1Answer}-${p2Answer}-${qrWord}-${riddle.a}`;

  return { puzzles, finalCode };
}
